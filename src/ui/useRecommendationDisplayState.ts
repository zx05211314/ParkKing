import { useMemo } from 'react'
import { buildRecommendationDisplayState } from './recommendationDisplayStateResult'
import type {
  RecommendationTargetLike,
  UseRecommendationDisplayStateOptions,
  UseRecommendationDisplayStateResult,
} from './recommendationDisplayStateTypes'

export const useRecommendationDisplayState = <TTarget extends RecommendationTargetLike>({
  filterQuery,
  markedSpacesOnly,
  hideReportedIllegal,
  illegalFeedbackHiddenCount,
  actionFilter,
  actionFilterHiddenCount,
  includeInferred,
  radiusMeters,
  riskMode,
  defaultSegmentActionFilter,
  defaultRadiusMeters,
  defaultRiskMode,
  actionFilterLabels,
  riskModeLabels,
  favoriteAddresses,
  searchAnchor,
  addressRecommendationTargets,
  reportsBySegment,
  routeEtaBySegmentId,
  recommendationRankMode,
  routeEtaStatus,
  routeEtaError,
  searchLocation,
  searchLocationLabel,
  displaySegments,
}: UseRecommendationDisplayStateOptions<TTarget>): UseRecommendationDisplayStateResult<TTarget> =>
  useMemo(
    () =>
      buildRecommendationDisplayState({
        filterQuery,
        markedSpacesOnly,
        hideReportedIllegal,
        illegalFeedbackHiddenCount,
        actionFilter,
        actionFilterHiddenCount,
        includeInferred,
        radiusMeters,
        riskMode,
        defaultSegmentActionFilter,
        defaultRadiusMeters,
        defaultRiskMode,
        actionFilterLabels,
        riskModeLabels,
        favoriteAddresses,
        searchAnchor,
        addressRecommendationTargets,
        reportsBySegment,
        routeEtaBySegmentId,
        recommendationRankMode,
        routeEtaStatus,
        routeEtaError,
        searchLocation,
        searchLocationLabel,
        displaySegments,
      }),
    [
      actionFilter,
      actionFilterHiddenCount,
      actionFilterLabels,
      addressRecommendationTargets,
      defaultRadiusMeters,
      defaultRiskMode,
      defaultSegmentActionFilter,
      displaySegments,
      favoriteAddresses,
      filterQuery,
      hideReportedIllegal,
      illegalFeedbackHiddenCount,
      includeInferred,
      markedSpacesOnly,
      radiusMeters,
      recommendationRankMode,
      reportsBySegment,
      riskMode,
      riskModeLabels,
      routeEtaBySegmentId,
      routeEtaError,
      routeEtaStatus,
      searchAnchor,
      searchLocation,
      searchLocationLabel,
    ],
  )
